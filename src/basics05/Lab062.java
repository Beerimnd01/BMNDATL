package src.basics05;

public class Lab062 {
    public static void main(String[] args) {

        //While Loop
        int i=1; //initialization
        while(i<=10){// condition
            System.out.println(i);//code execution
            i++;//pre or post increment

        }
        for (int j = 1; j <=10 ; j++) {
            System.out.println(j);
        }


    }
}
